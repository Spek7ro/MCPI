a = int(input("Ingrese el primer número (a): "))
b = int(input("Ingrese el segundo número (b): "))

# División entera
division_entera = a // b

# Resto de la división
resto = a % b

# Potencia a elevado a b
potencia = a ** b

print(f"División entera de {a} entre {b}: {division_entera}")
print(f"Resto de la división de {a} entre {b}: {resto}")
print(f"{a} elevado a la {b} es: {potencia}")

